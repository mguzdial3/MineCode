	private int currIndex=0;
	private int currAction = 0;

	private int startingIndex = 4;
	private int groundLevel = 1;
	private int width =32;

	private float timer;
	private float maxTimer = 0.1f;

	private bool yesRunIt = false;

	// Use this for initialization
	void Start () {
		if (yesRunIt) {
			currIndex = GetIndexByPosition(transform.position);		
		}
	}

	public void BeginRun(double [,] pol){
		yesRunIt = true;
		policy = pol;
		currIndex = GetIndexByPosition(transform.position);

	}

	void Update(){
		if(yesRunIt){
			if(timer<maxTimer){
				timer+=Time.deltaTime;
			}
			else{
				timer=0;

				//Get Action Based on Index
				double max = -10000;
				int choice = -1;

				//Get max
				for(int i = 0; i<4; i++){
					if(policy[currIndex,i]>max){
						choice = i;
						max = policy[currIndex,i];
					}
				}

				int blockId = Map.Instance.GetBlockIdentityAtPosition((int)transform.position.x,groundLevel,(int)transform.position.z);

				if(blockId==4 || blockId == 5){
					float random = Random.Range(0f,1f);
					if(random<0.25f){
						choice=0;
					}
					else if(random<0.5f){
						choice=1;
					}
					else if(random<0.75f){
						choice=2;
					}
					else if(random<1f){
						choice=3;
					}

				}
				else{
					float random = Random.Range(0f,4f);
					if(random>1){
						//do smart move
					}
					else{
						if(random<0.25f){
							choice=0;
						}
						else if(random<0.5f){
							choice=1;
						}
						else if(random<0.75f){
							choice=2;
						}
						else if(random<1f){
							choice=3;
						}
					}
				}

				if(choice!=-1){
					currAction = choice;
					Vector3 newPos =transform.position;
					if(choice==0){ //Down
						newPos+=Vector3.forward;
					}
					else if(choice==1){//Up
						newPos+=Vector3.back;
					}
					else if(choice==2){//Right
						newPos+=Vector3.right;
					}
					else if(choice==3){//Left
						newPos+=Vector3.left;
					}

					if(Map.Instance.GetBlockIdentityAtPosition((int)newPos.x,(int)newPos.y,(int)newPos.z)!=2 && GetIndexByPosition(newPos)>0){
						transform.position = newPos;
						currIndex = GetIndexByPosition(transform.position);
					}

				}
			}
		}	
	}

	public int GetIndexByPosition(Vector3 pos){
		int xPos = (int)pos.x;
		int zPos = (int)pos.z;

		int xIndex = xPos-Map.Instance.Minimum.x-startingIndex;
		int zIndex = zPos -Map.Instance.Minimum.z - startingIndex;
		int index = zIndex*width+xIndex;

		return index;
	}

	public int GetCurrentIndex(){
		return currIndex;
	}

	public int GetCurrentAction(){
		return currAction;
	}




